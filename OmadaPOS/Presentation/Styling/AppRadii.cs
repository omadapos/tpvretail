namespace OmadaPOS.Presentation.Styling;

/// <summary>
/// Corner-radius tokens for the POS Design System.
/// Applied to RoundedPanel.CornerRadius and ElegantButtonStyles rounded paths.
/// </summary>
public static class AppRadii
{
    /// <summary>6px — subtle rounding for small controls and inputs.</summary>
    public const int Small  = 6;

    /// <summary>8px — scan panel, compact cards.</summary>
    public const int Compact = 8;

    /// <summary>10px — standard for action buttons.</summary>
    public const int Medium = 10;

    /// <summary>12px — panels with gradient backgrounds (totals, summary).</summary>
    public const int Panel  = 12;

    /// <summary>14px — large cards.</summary>
    public const int Large  = 14;

    /// <summary>16px — popup dialogs, split payment card.</summary>
    public const int Dialog = 16;

    /// <summary>18px — customer screen panels with heavy rounding.</summary>
    public const int Heavy  = 18;
}
